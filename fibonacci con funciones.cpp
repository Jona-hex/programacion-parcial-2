float fibonnaci(int sum)
{
	if (sum==1)
	{
 		return 0;
	}
	else if (sum==2)
 	{
 		return 1;
 	}
 	else
 	{
 		return fibonnaci(sum-1)+fibonnaci(sum-2);
	}
}
